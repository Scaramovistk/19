int	ft_iterative_factorial(int nb)
{
	int n;
	int b;

	n = 1;
	b = 1;
	if (nb < 0)
		return (0);
	else if (nb == 0)
		return (1);
	while (n <= nb)
	{
		b = b * n;
		n++;
	}
	return (b);
}
