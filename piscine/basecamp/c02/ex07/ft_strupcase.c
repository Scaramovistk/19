#include <stdio.h>

char *ft_strupcase(char *str)
{
	int i;

	i = 0;
	while (*(str + i) != '\0')
	{
		if (*(str + i) >= 'a' && *(str + i) <= 'z')
		{
			*(str + i) = *(str + i) - 32;
		}
		i++;
	}
	return (str);
}

int    main(void)
{
    char str[100] = "heLLo tHIS is a TEST!!!! Did it work?";
    printf("result: %s", ft_strupcase(str));
    return (0);
}
