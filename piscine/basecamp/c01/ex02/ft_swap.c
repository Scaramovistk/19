#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int swap;

	swap = *b;
	*b = *a;
	*a = swap;
}

int main(void)
{
	int a;
	int b;

	a = 1;
	b = 2;
	printf("before: %d %d\n", a, b);
	ft_swap(&a, &b);
	printf("before: %d %d\n", a, b);
}
