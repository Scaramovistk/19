#include <stdio.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int i;

	i = 0;
	while (*(src + i) != '\0' && i < n)
	{
		*(dest + i) = *(src + i);
		i++;
	}
	while (i < n)
	{
		*(dest + i) = '\0';
		i++;
	}
	return (dest);
}

int    main(void)
{
	int n = 2;
    char src[] = "hello";
	char dest[] = "yello";

    ft_strncpy(dest, src, n);
    printf("content: %s", dest);
    return (0);
}
